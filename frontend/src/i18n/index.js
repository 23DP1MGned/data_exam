const storedLocale = typeof window !== 'undefined' ? window.localStorage.getItem(STORAGE_KEY) : null
const locale = ref(storedLocale && messages[storedLocale] ? storedLocale : DEFAULT_LOCALE)

function persistLocale(value) {
  if (typeof window !== 'undefined') {
    window.localStorage.setItem(STORAGE_KEY, value)
  }
}

function setLocale(value) {
  if (!messages[value]) return
  locale.value = value
  persistLocale(value)
}

function resolveMessage(path) {
  return path.split('.').reduce((current, key) => current?.[key], messages[locale.value])
}

function interpolate(template, params = {}) {
  return Object.entries(params).reduce(
    (result, [key, value]) => result.replaceAll(`{${key}}`, value),
    template
  )
}

function t(path, params = {}) {
  const message = resolveMessage(path)
  if (typeof message !== 'string') return path
  return interpolate(message, params)
}

export function useLocale() {
  return {
    locale,
    currentLocale: computed(() => locale.value),
    setLocale,
    t,
    languageOptions: computed(() => [
      { value: 'lv', label: messages[locale.value].language.latvian },
      { value: 'en', label: messages[locale.value].language.english }
    ])
  }
}
